interface SidebarProps {
  isOpen: boolean;
  onToggle: () => void;
}

export default function Sidebar({ isOpen, onToggle }: SidebarProps) {
  const pathname = usePathname();
  const [isHovered, setIsHovered] = useState(false);

  // On desktop: show collapsed (72px), expand on hover
  // On mobile: hidden by default, show full width when open
  const isExpanded = isHovered || isOpen;

  return (
    <>
      {/* Mobile overlay */}
      {isOpen && (
        <div
          className="sidebar-overlay"
          onClick={onToggle}
        />
      )}

      {/* Sidebar */}
      <aside
        className={`sidebar ${isExpanded ? 'expanded' : 'collapsed'} ${isOpen ? 'open' : ''}`}
        onMouseEnter={() => setIsHovered(true)}
        onMouseLeave={() => setIsHovered(false)}
      >
        {/* Logo */}
        <div className="sidebar-logo">
          <div className="sidebar-logo-inner">
            <div className="sidebar-logo-icon">
              <svg width="22" height="22" viewBox="0 0 24 24" fill="none" stroke="white" strokeWidth="2">
                <path d="M12 2v20M17 5H9.5a3.5 3.5 0 0 0 0 7h5a3.5 3.5 0 0 1 0 7H6" />
              </svg>
            </div>
            <span className={`sidebar-logo-text ${isExpanded ? 'expanded' : 'collapsed'}`}>
              inDebt
            </span>
          </div>
        </div>

        {/* Navigation */}
        <nav className="sidebar-nav">
          <ul className="sidebar-nav-list">
            {navItems.map((item) => {
              const isActive = pathname === item.href;
              return (
                <li key={item.id}>
                  <Link
                    href={item.href}
                    className={`sidebar-nav-link ${isActive ? 'active' : ''}`}
                    title={item.label}
                    onClick={() => { if (isOpen) onToggle(); }}
                  >
                    <span className="sidebar-nav-icon">{item.icon}</span>
                    <span className={`sidebar-nav-label ${isExpanded ? 'expanded' : 'collapsed'}`}>
                      {item.label}
                    </span>
                  </Link>
                </li>
              );
            })}
          </ul>
        </nav>

        {/* User avatar */}
        <div className="sidebar-user">
          <div className="sidebar-user-inner">
            <div className="sidebar-user-avatar">
              ZM
            </div>
            <div className={`sidebar-user-info ${isExpanded ? 'expanded' : 'collapsed'}`}>
            </div>
          </div>
        </div>
      </aside>
    </>
  );
}
